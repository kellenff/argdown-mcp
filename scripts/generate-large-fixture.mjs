#!/usr/bin/env node
/**
 * generate-large-fixture.mjs
 *
 * Generates tests/fixtures/large.argdown — a valid Argdown document >= 3 MB.
 * Idempotent: running it multiple times produces the same output.
 *
 * Usage:
 *   node scripts/generate-large-fixture.mjs
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, "../tests/fixtures/large.argdown");

const TARGET_BYTES = 3 * 1024 * 1024; // 3 MB

// Deterministic pseudo-random number generator (LCG)
function makePrng(seed) {
  let s = seed >>> 0;
  return function () {
    s = (Math.imul(1664525, s) + 1013904223) >>> 0;
    return s;
  };
}

const rand = makePrng(42);

function randInt(min, max) {
  return min + (rand() % (max - min + 1));
}

const lines = [];

lines.push("# Generated Large Argdown Fixture");
lines.push("# This file is machine-generated by scripts/generate-large-fixture.mjs.");
lines.push("# Do not edit by hand.");
lines.push("");
lines.push("## Overview");
lines.push("");
lines.push(
  "[Root Claim]: This document contains a large number of statements and arguments for performance testing."
);
lines.push("");

const STATEMENT_COUNT = 6000;
const SECTION_EVERY = 200;

// Track defined statement indices so we can reference earlier ones
const defined = [0]; // index 0 = Root Claim

for (let i = 1; i <= STATEMENT_COUNT; i++) {
  // Insert section headings periodically
  if (i % SECTION_EVERY === 1) {
    const sectionNum = Math.floor(i / SECTION_EVERY) + 1;
    lines.push(`## Section ${sectionNum}`);
    lines.push("");
  }

  const stmtName = `Statement ${i}`;
  const description = `This is automatically generated statement number ${i} used for load and performance testing of the Argdown parser.`;
  lines.push(`[${stmtName}]: ${description}`);

  // Add 1-3 relations to previously-defined statements
  const relationCount = randInt(1, 3);
  const alreadyUsed = new Set();

  for (let r = 0; r < relationCount; r++) {
    if (defined.length <= 1) break;
    let refIdx;
    let attempts = 0;
    do {
      const pick = randInt(0, defined.length - 1);
      refIdx = defined[pick];
      attempts++;
    } while (alreadyUsed.has(refIdx) && attempts < 10);

    if (alreadyUsed.has(refIdx)) break;
    alreadyUsed.add(refIdx);

    const relation = rand() % 2 === 0 ? "+" : "-";
    const refName = refIdx === 0 ? "Root Claim" : `Statement ${refIdx}`;
    lines.push(`  ${relation} [${refName}]`);
  }

  lines.push("");
  defined.push(i);
}

// Add a bank of arguments at the end
lines.push("## Arguments");
lines.push("");

const ARG_COUNT = 500;
for (let i = 1; i <= ARG_COUNT; i++) {
  const argName = `Argument ${i}`;
  lines.push(
    `<${argName}>: Automatically generated argument ${i} for performance testing.`
  );

  // Add 1-2 premise relations
  const premiseCount = randInt(1, 2);
  const used = new Set();
  for (let p = 0; p < premiseCount; p++) {
    if (defined.length === 0) break;
    let refIdx;
    let attempts = 0;
    do {
      const pick = randInt(0, defined.length - 1);
      refIdx = defined[pick];
      attempts++;
    } while (used.has(refIdx) && attempts < 10);
    if (used.has(refIdx)) break;
    used.add(refIdx);

    const relation = rand() % 3 === 0 ? "-" : "+";
    const refName = refIdx === 0 ? "Root Claim" : `Statement ${refIdx}`;
    lines.push(`  ${relation} [${refName}]`);
  }

  lines.push("");
}

let content = lines.join("\n");

// If somehow under target, repeat a padding block until we hit it
while (Buffer.byteLength(content, "utf8") < TARGET_BYTES) {
  const extra = [];
  for (let i = STATEMENT_COUNT + 1; i <= STATEMENT_COUNT + 500; i++) {
    extra.push(
      `[Padding Statement ${i}]: Additional padding statement to reach the minimum file size target.`
    );
    extra.push("  + [Root Claim]");
    extra.push("");
  }
  content += extra.join("\n");
}

writeFileSync(OUTPUT_PATH, content, "utf8");

const bytes = Buffer.byteLength(content, "utf8");
console.log(`Written: ${OUTPUT_PATH}`);
console.log(`Size: ${bytes.toLocaleString()} bytes (${(bytes / 1024 / 1024).toFixed(2)} MB)`);

if (bytes < TARGET_BYTES) {
  console.error(`ERROR: file is smaller than ${TARGET_BYTES} bytes!`);
  process.exit(1);
}
